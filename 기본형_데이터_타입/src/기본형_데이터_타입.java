
public class 기본형_데이터_타입 {

	public static void main(String[] args) {
	
		boolean isFun = true; // boolean은 true , false만 있음
		System.out.println(isFun);
		
		char c = 'f'; // char은 싱글커터 사용 , 문자 하나만 표현 가능       
		
		int x = 59; // 정수 표현
		
		long big = 123456789L; // 더 큰 정수를 표현할 때 , 롱 타입은 뒤에 영어ㅣ을 넣어야됨
	
	    float f = 32.4f; // 실수를 표현 할 때에는 f를 뒤에다 붙어야됨
        
	    double d = 12345678.9; // 더 큰 실수를 표현할 때
	} 

}
